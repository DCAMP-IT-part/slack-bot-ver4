import re

def mask_personal_info(text: str) -> str:
    """
    이메일, 전화번호, 차량번호 등을 정규식으로 찾아서 *** 처리합니다.
    필요에 따라 정규식 패턴을 추가하거나 수정할 수 있습니다.
    """
    # 이메일 마스킹 (예: user@example.com -> ***@***.***)
    text = re.sub(
        r"[a-zA-Z0-9._%+-]+@[a-zA-Z0-9.-]+\.[a-zA-Z]+",
        "***@***.***",
        text
    )

    # 전화번호 마스킹 (예: 010-1234-5678, 02-123-4567 등 -> ***-****-****)
    text = re.sub(
        r"\b\d{2,3}-\d{3,4}-\d{4}\b",
        "***-****-****",
        text
    )

    # 차량번호 마스킹 (예: 12가 3456, 123가 4567 등 -> *** ****)
    text = re.sub(
        r"\b\d{2,3}[가-힣A-Za-z]\s?\d{4}\b",
        "*** ****",
        text
    )

    return text
